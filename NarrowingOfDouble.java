class NarrowingOfDouble
{
  public static void main(String[]args)
  {
    double d= 132.244556;

    byte b=(byte)d;
    System.out.println(b);

    char ch=(char)d;
    System.out.println(ch);

    short s=(short)d;
    System.out.println(s);

    int i=(int)d;
    System.out.println(i);

    long l=(long)d;
    System.out.println(l);

    float f=(float)d;
    System.out.println(f);
   
  }
}