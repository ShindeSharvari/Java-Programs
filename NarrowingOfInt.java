class NarrowingOfInt
{
  public static void main(String[]args)
  {
    int i=565788;

    byte b=(byte)i;
    System.out.println(b);

    char ch=(char)i;
    System.out.println(ch);

    short s=(short)i;
    System.out.println(s);

    long l=(long)i;
    System.out.println(l);

    float f=(float)i;
    System.out.println(f);

    double d=(double)i;
    System.out.println(d);
   
  }
}