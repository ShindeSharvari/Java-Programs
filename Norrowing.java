class Norrowing
{
  public static void main(String[]args)
  {
   int a=(int)123.123;
   System.out.println(a);

   char ch=(char)100.0343f;
   System.out.println(ch);
   System.out.println((char)123);

   byte i=(byte)128;
   System.out.println(i);

   char c=(char)6343523;
   System.out.println(c);
  }
}