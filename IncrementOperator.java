class IncrementOperator
{
  public static void main(String[] args)
  {

   int a=7;
   System.out.println(++a);

   System.out.println(++a);

   System.out.println(a++);

   System.out.println(a);

   System.out.println(a++);

   System.out.println(++a);

   System.out.println(++a + a++);

   System.out.println(a++ + a++);

  }
}