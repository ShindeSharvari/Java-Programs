class Game 
{
	public void goForward(int steps) 
	{
		System.out.println("Moved "+steps+" forward");
	}
	public void goBackward(int steps) 
	{
		System.out.println("Moved "+steps+" left");
	}
	public void goLeft(int steps) 
	{
		System.out.println("Moved "+steps+" back");
	}
	public void goRight(int steps) 
	{
		System.out.println("Moved "+steps+" right");
	}
}
